    public void cargarPaquetes(){
        
        btnAgregar.setEnabled(false);
        btnRemover.setEnabled(false);
        
        ArrayList<Paquete> paquetesEnviados = new ArrayList<Paquete>(this.getEnvio().getPaquetes());
        Collections.sort(paquetesEnviados);

        //Por defecto asumimos que todos los paquetes fueron entregados
        lstEntregados.setListData(paquetesEnviados.toArray(Paquete[]::new));

        lstNoEntregados.setListData(new Paquete[0]);
    }

