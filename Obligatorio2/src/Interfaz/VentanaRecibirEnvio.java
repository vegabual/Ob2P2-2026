    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(this, "Una vez recibido el envio, no se puede modificar. \nEstá seguro de los cambios?", "¡ATENCIÓN!",
                JOptionPane.YES_NO_OPTION);
        
        if(confirmacion == JOptionPane.YES_OPTION){
            ArrayList<Paquete> paquetesABorrar = this.getPaquetesDeLista(lstNoEntregados);
            this.getSistema().RecibirEnvio(this.getEnvio(), paquetesABorrar);
            
            //Mostrar mensaje de exito, cerrar la ventana y abrir la anterior
            JOptionPane.showMessageDialog(this, "Envío recibido correctamente.");
            dispose();
            VentanaRecepcion nueva = new VentanaRecepcion(sistema);
            nueva.setVisible(true);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

