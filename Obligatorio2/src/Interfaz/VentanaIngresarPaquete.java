    private void borrarUltimoChar(JTextField campo){
        String texto = campo.getText();
        if(texto.length() > 0){
            campo.setText(texto.substring(0, texto.length() - 1));
        }
    }
    
    private void calcularPrecio(){
        String depto = cmbDepartamento.getSelectedItem().toString();
        String pesoStr = txtPeso.getText().trim();
        
        if(depto != "Seleccionar" && stringEsParseableAInt(pesoStr) && Integer.parseInt(pesoStr) > 0){
            int peso = Integer.parseInt(pesoStr);
            String precioCalculado = "$ " + this.getSistema().precioPaquete(depto, peso);
            lblPrecioCalculado.setText(precioCalculado);
        } else{
            lblPrecioCalculado.setText("Ingresar departamento y peso para calcular");
        }
    }
    
    private void cargarClientes(){
        ArrayList<String> nombresClientes = new ArrayList<String>();
        nombresClientes.add("Seleccionar");
        Iterator<Cliente> it = this.getSistema().getClientes().iterator();
        while(it.hasNext()){ //recorro el arraylist de clientes y los agrego de a uno al modelo
            Cliente c = it.next();
            nombresClientes.add(c.getNombre());
        }
        cmbCliente.setModel(new DefaultComboBoxModel(nombresClientes.toArray())); //Asigno la lista modelo al jlist
    }
    
