    private void calcularPrecios(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularPrecios
        String depto = cmbDepartamento.getSelectedItem().toString();
        String pesoStr = txtPeso.getText().trim();
        
        if(depto != "Seleccionar" && stringEsParseableAInt(pesoStr) && Integer.parseInt(pesoStr) > 0){
            int peso = Integer.parseInt(pesoStr);
            String precioCalculado = "$ " + this.getSistema().precioPaquete(depto, peso);
            lblPrecioCalculado.setText(precioCalculado);
        } else{
            lblPrecioCalculado.setText("Ingresar departamento y peso para calcular");
        }
    }//GEN-LAST:event_calcularPrecios

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
            boolean esValido = true;
            String id = txtId.getText().trim();

            Cliente cliente = this.getSistema().encontrarCliente(cmbCliente.getSelectedItem().toString());
            String destinatario = txtDestinatario.getText().trim();
            String direccion = txtDireccion.getText().trim();
            String departamento = cmbDepartamento.getSelectedItem().toString().trim();

            //Parseo de fecha -> Tira excepcion si no son valores numericos
            int dia = Integer.parseInt(txtFechaDia.getText().trim());                                     
            int mes = Integer.parseInt(txtFechaMes.getText().trim());
            int anio = Integer.parseInt(txtFechaAnio.getText().trim());
            Fecha fecha = Fecha.parseFecha(dia, mes, anio); //-> tira excepcion si es una fecha invalida

            //Parseo de peso -> Tira excepcion si no es un valor numerico
            int peso = Integer.parseInt(txtPeso.getText().trim());

            //Validar
            esValido &= !(id.isEmpty() || cliente == null || destinatario.isEmpty() || direccion.isEmpty() || departamento.equalsIgnoreCase("Seleccionar") || peso <= 0); //Ningun valor esta vacio
            if(esValido){
                //Guardar en el sistema
                if(!sistema.guardarPaquete(id, cliente, fecha, destinatario, direccion, departamento, peso)){
                    JOptionPane.showMessageDialog(this, "Ya existe un Paquete con ese identificador, ingrese otro.", "Error al guardar paquete", ERROR_MESSAGE); //Pide otro nombre
                }
                else{
                    JOptionPane.showMessageDialog(this, "Paquete guardado correctamente.");
                    VentanaPaquete nueva = new VentanaPaquete(sistema);
                    nueva.setVisible(true);
                    this.dispose();
                } 
            }
            else{
                JOptionPane.showMessageDialog(this, "Debe ingresar un todos los datos.", "Error al guardar paquete", ERROR_MESSAGE);
            }
        } catch (NumberFormatException ex){
            //Loggear el error
            JOptionPane.showMessageDialog(this, "La fecha y el peso deben ser valores numéricos.", "Error al guardar paquete", ERROR_MESSAGE);
        } catch(ErrorFechaNoValidaException ex) {
            //Loggear el error
            JOptionPane.showMessageDialog(this, "El formato de fecha es inválida o se ingresó una fecha incorrecta.", "Error al guardar paquete", ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed
    
    private void validarPeso(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validarPeso
        String peso = txtPeso.getText();
        if(peso.length() > 0 && evt.getKeyCode() != KeyEvent.VK_ENTER){
            if(stringEsParseableAInt(peso)){
                this.calcularPrecio();
            } else{
                JOptionPane.showMessageDialog(this, "Solo se pueden ingresar numeros enteros.", "Input inválido", ERROR_MESSAGE);
                while(!stringEsParseableAInt(peso) && peso.length() > 0){
                    borrarUltimoChar(txtPeso);
                    peso = txtPeso.getText();
                }
                this.calcularPrecio();
            }
        } else if(peso.length() == 0) {
            this.calcularPrecio();
        }
    }//GEN-LAST:event_validarPeso

    private void validarDia(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validarDia
        String dia = txtFechaDia.getText().trim();
        if(dia.length() > 0 && evt.getKeyCode() != KeyEvent.VK_ENTER){
            boolean esValido = stringEsParseableAInt(dia) && Integer.parseInt(dia) > 0 && Integer.parseInt(dia) < 32;
            if(!esValido){ //Si el dia no es un numero, o no estra entre 1 y 31, mostrar mensaje de error y borrar el dato
                JOptionPane.showMessageDialog(this, "Solo se pueden ingresar numeros del 1 al 31.", "Input inválido", ERROR_MESSAGE);
                while(!esValido && dia.length() > 0){
                    borrarUltimoChar(txtFechaDia);
                    dia = txtFechaDia.getText().trim();
                    esValido = stringEsParseableAInt(dia) && Integer.parseInt(dia) > 0 && Integer.parseInt(dia) < 32;
                }
            }
        }
    }//GEN-LAST:event_validarDia

