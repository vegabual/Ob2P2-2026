    //Metodo para cargar las tarifas en la tabla
    private void cargarTarifas(){
        
        DefaultTableModel modelo = new DefaultTableModel(); //Defino una nueva tabla
        modelo.addColumn("Zona"); //Le pongo el nombre a cada columna
        modelo.addColumn("Categoria 1");
        modelo.addColumn("Categoria 2");
        modelo.addColumn("Categoria 3");
        modelo.addColumn("Categoria 4");
        
        //Copio de la lista para no modificar el orden de la original
        ArrayList<Tarifa> ordenada = new ArrayList<>(sistema.getTarifas());
        
        //Para cada tarifa la agrego a la tabla
        for(int i = 0; i < ordenada.size(); i++){
            Tarifa t = ordenada.get(i);
            modelo.addRow(new Object[]{t.getNombre(), t.getPrecioCat1(), t.getPrecioCat2(), t.getPrecioCat3(),t.getPrecioCat4()});
        }
        
        ListaTarifas.setModel(modelo);  
    }

